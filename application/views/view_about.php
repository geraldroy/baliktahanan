		<p class="lead">
We are a group of four university students who, for various reasons, decided to join a hackathon. The theme is transportation. Our web app, BalikTahanan, was inspired by the hustle and bustle of the big city, the congested roads with vendors toiling about, and the patches of shanties squatting on still undeveloped land. Metro Manila is a hub of development in comparison to far-flung provinces, where growth is little to none. In those very provinces, men and women looking for something greater--often jobs and better living conditions--decide to take the plunge and move to the big city, pawning and selling their belongings on the high-risk, high-reward journey. In the city, many believe, a job awaits, money awaits, greener pastures await. Every year, a large number of what we call "rural to urban immigrants" take the gamble. Not many succeed. Often, the city isn't what they make it out to be, and they end up homeless, joining the throes of people squatting on undeveloped land. Many decide to live there permanently, having already abandoned their life in the paddies for a life in the slums. Some, however, desire to go back home, but are unable to pay for their transport fees. Our web app, BalikTahanan, is made for that demographic. We want to bring the homeless back home. That is what we do</p><br>
